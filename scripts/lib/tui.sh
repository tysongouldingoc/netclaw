#!/usr/bin/env bash
# NetClaw installer — pure-bash TUI: logo, arrow-key menu, multi-select checklist.
# No external dependencies (no whiptail/dialog). Degrades to plain output when
# stdout is not a TTY.

# ── colors (VibeOps neon palette — vibeopsforum.com) ─────────────
T_PINK='\033[38;5;198m'      # neon pink   (#ff2d9f)
T_PINKLT='\033[38;5;212m'    # light pink  (#ff9bd2)
T_CYAN='\033[38;5;45m'       # neon cyan   (#38e8ff)
T_CYANLT='\033[38;5;159m'    # pale cyan   (#b9f7ff)
T_YELLOW='\033[38;5;228m'    # neon yellow (#f6ff6a)
T_WHITE='\033[97m'
T_BOLD='\033[1m'
T_DIM='\033[2m'
T_REV='\033[7m'
T_NC='\033[0m'

tui_is_tty() { [ -t 0 ] && [ -t 1 ]; }

